public class HeightEntity {
    private String name;
    private String password;
    private String cupSize;
    private String job;
    private int age;
    private int height;
    private int start_work;
    private int stop_work;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getCupSize() {
        return cupSize;
    }

    public void setCupSize(String cupSize) {
        this.cupSize = cupSize;
    }

    public String getJob() {
        return job;
    }

    public void setJob(String job) {
        this.job = job;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getStart_work() {
        return start_work;
    }

    public void setStart_work(int start_work) {
        this.start_work = start_work;
    }

    public int getStop_work() {
        return stop_work;
    }

    public void setStop_work(int stop_work) {
        this.stop_work = stop_work;
    }
}
